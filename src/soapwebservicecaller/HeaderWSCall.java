package soapwebservicecaller;

public class HeaderWSCall {

    private String name;
    private String value;

    public HeaderWSCall(String name, String value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

}
